import torch
import numpy as np
from sklearn.metrics import precision_recall_fscore_support, precision_recall_curve, auc

def evaluate_model(model, X_test, y_test, device='cpu'):
    """
    Computes sample-wise reconstruction error to identify anomalies.
    Finds the mathematical optimal threshold to maximize F1-score and prints key metrics.
    """
    model.eval()
    model.to(device)

    X_test_tensor = torch.FloatTensor(X_test).to(device)

    with torch.no_grad():
        reconstructed = model(X_test_tensor)

    # Calculate sample-wise Mean Squared Error (MSE) across all feature dimensions
    mse = torch.mean((X_test_tensor - reconstructed) ** 2, dim=1)
    reconstruction_errors = mse.cpu().numpy()

    # 1. Generate Precision-Recall Curve metrics and calculate PR-AUC
    precisions, recalls, thresholds = precision_recall_curve(y_test, reconstruction_errors)
    pr_auc = auc(recalls, precisions)

    # 2. Locate the optimal decision threshold maximizing the F1-Score balance
    f1_scores = 2 * (precisions * recalls) / (precisions + recalls + 1e-8)
    best_idx = np.argmax(f1_scores)
    optimal_threshold = thresholds[best_idx]

    # 3. Classify records: If reconstruction error exceeds threshold -> Fraud (1), else Normal (0)
    predictions = (reconstruction_errors > optimal_threshold).astype(int)

    # 4. Extract final evaluation scores for reporting
    precision, recall, f1, _ = precision_recall_fscore_support(y_test, predictions, average='binary')

    print("\n=== EVALUATION PERFORMANCE REPORT ===")
    print(f"Optimal Reconstruction Threshold: {optimal_threshold:.6f}")
    print(f"PR-AUC (Area Under PR Curve):    {pr_auc:.4f}")
    print(f"Precision (Alarm Accuracy):      {precision:.4f}")
    print(f"Recall (Fraud Detection Rate):   {recall:.4f}")
    print(f"F1-Score (Harmonic Balance):     {f1:.4f}")
    print("=====================================\n")

    return {
        'reconstruction_errors': reconstruction_errors,
        'optimal_threshold': optimal_threshold,
        'predictions': predictions,
        'precision': precision,
        'recall': recall,
        'f1_score': f1,
        'pr_auc': pr_auc
    }